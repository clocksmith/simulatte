#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const REGISTRY_PATH = path.join(ROOT, 'public', 'data', 'pipeline-job-registry.json');
const OUTPUT_PATH = path.join(ROOT, 'docs', 'pipeline-job-matrix.md');
const require = createRequire(import.meta.url);
const jobInterface = require('../public/shared/contracts/pipeline-job-interface.js');

export function renderPipelineJobMatrix(registry, options = {}) {
  const root = options.root || ROOT;
  jobInterface.validateRegistry(registry, {
    pathExists: (candidate) => fs.existsSync(path.join(root, candidate.split('#')[0])),
  });
  const sources = loadEvidenceSources(registry, root);
  const matrix = jobInterface.buildJobMatrix(registry);
  const strategyUsage = collectStrategyUsage(registry);
  const lines = [
    '<!-- Generated by tools/render-pipeline-job-matrix.mjs. Do not edit by hand. -->',
    '',
    '# Pipeline jobs, surface configurations, and tiers',
    '',
    'The job is the stable abstraction. `/` and `/blank/` configure the same eight jobs with different boundaries and implementations. A shared strategy ID means the surfaces share the strategy contract; owner paths show whether they share code today.',
    '',
    '## Job configuration',
    '',
    '| Job | Main `/` — Autonomy | Blank `/blank/` | Shared strategy contracts |',
    '| --- | --- | --- | --- |',
  ];

  for (const row of matrix) {
    const autonomy = row.surfaces.autonomy;
    const blank = row.surfaces.blank;
    const shared = intersect(
      autonomy.strategies.map((selection) => selection.id),
      blank.strategies.map((selection) => selection.id)
    );
    lines.push(`| ${escapeCell(row.job.label)} | ${escapeCell(bindingLabel(autonomy))} | ${escapeCell(bindingLabel(blank))} | ${escapeCell(shared.map((id) => strategyLabel(registry, id)).join('; ') || 'None')} |`);
  }

  lines.push(
    '',
    '## Strategy configuration',
    '',
    '| Job | Strategy | Kind | Main `/` | Blank `/blank/` |',
    '| --- | --- | --- | --- | --- |'
  );
  for (const job of registry.jobs) {
    for (const strategy of registry.strategies.filter((row) => row.jobId === job.id)) {
      const usage = strategyUsage.get(strategy.id);
      lines.push(`| ${escapeCell(job.label)} | ${escapeCell(strategy.label)} | ${strategy.kind} | ${usage.autonomy || '—'} | ${usage.blank || '—'} |`);
    }
  }

  lines.push(
    '',
    '## Performance references',
    '',
    'These values are read from the referenced receipts during generation. Candidate screening status and product execution status are intentionally separate: a strategy can execute under a product-specific contract while remaining unpromoted by the general model-selection frontier.',
    '',
    '| Strategy | Population or lane | Quality | Runtime | Gate | Source |',
    '| --- | --- | --- | --- | --- | --- |'
  );
  for (const strategy of registry.strategies) {
    for (const reference of strategy.performanceRefs) {
      const source = sources.get(reference.sourceId);
      const summary = summarizeReference(source, reference);
      lines.push(`| ${escapeCell(strategy.label)} | ${escapeCell(summary.population)} | ${escapeCell(summary.quality)} | ${escapeCell(summary.runtime)} | ${escapeCell(summary.gate)} | [${reference.sourceId}](../${source.spec.path}) |`);
    }
  }

  lines.push(
    '',
    '## Ownership',
    '',
    '| Job | Main `/` owners | Blank `/blank/` owners |',
    '| --- | --- | --- |'
  );
  for (const row of matrix) {
    lines.push(`| ${escapeCell(row.job.label)} | ${formatOwners(row.surfaces.autonomy.owners)} | ${formatOwners(row.surfaces.blank.owners)} |`);
  }
  lines.push(
    '',
    '## Updating the matrix',
    '',
    '1. Change job, strategy, or surface configuration in `public/data/pipeline-job-registry.json`.',
    '2. Generate new performance receipts with their owning evaluator; do not copy metrics into the registry.',
    '3. Run `npm run pipeline:matrix:sync`. `npm run check:model-candidates` checks registry references and generated drift.',
    ''
  );
  return `${lines.join('\n')}\n`;
}

function loadEvidenceSources(registry, root) {
  return new Map(registry.evidenceSources.map((spec) => [
    spec.id,
    { spec, value: JSON.parse(fs.readFileSync(path.join(root, spec.path), 'utf8')) },
  ]));
}

function collectStrategyUsage(registry) {
  const usage = new Map(registry.strategies.map((strategy) => [strategy.id, {}]));
  for (const surface of registry.surfaces) {
    const resolved = jobInterface.resolveSurface(registry, surface.id);
    for (const binding of resolved.bindings) {
      for (const selection of binding.strategies) usage.get(selection.id)[surface.id] = selection.status;
    }
  }
  return usage;
}

function summarizeReference(source, reference) {
  if (reference.recordType === 'candidate') return summarizeCandidate(source, reference.recordId);
  return summarizeLane(source, reference.recordId);
}

function summarizeCandidate(source, candidateId) {
  const candidate = source.value.candidates?.find((row) => row.id === candidateId);
  if (!candidate) throw new Error(`${source.spec.id} does not contain candidate ${candidateId}`);
  const quality = Object.entries(candidate.quality || {})
    .filter(([, value]) => typeof value === 'number')
    .map(([key, value]) => `${metricLabel(key)} ${formatNumber(value)}`)
    .join('; ');
  const performance = candidate.performance || {};
  const runtime = [
    formatBytes(performance.downloadBytes),
    formatLatency(performance.coldLoadMs, 'cold'),
    formatLatency(performance.warmLatencyMs, 'warm'),
  ].filter(Boolean).join('; ');
  return {
    population: `${source.value.population?.id || source.value.task} (${source.value.population?.rowCount || '?'} rows${Number.isFinite(source.value.workload?.k) ? `; k=${source.value.workload.k}` : ''})`,
    quality,
    runtime,
    gate: candidate.clearsQualityGate ? 'clears quality gate' : 'not promoted by frontier',
  };
}

function summarizeLane(source, laneId) {
  const lane = source.value.lanes?.[laneId];
  if (!lane) throw new Error(`${source.spec.id} does not contain lane ${laneId}`);
  const metrics = lane.metrics || {};
  const guardrails = lane.guardrails || {};
  return {
    population: `${source.value.population?.id || source.spec.id} / ${lane.resolverId || laneId}`,
    quality: `${metrics.correct}/${metrics.probeCount} correct; ${metrics.wrongPlace || 0} wrong-place`,
    runtime: 'not recorded in this diagnostic',
    gate: `${guardrails.mustRefuseViolations || 0} refusal violations; ${guardrails.floorMisses || 0} floor misses`,
  };
}

function bindingLabel(binding) {
  return binding.phase == null ? binding.boundary : `Phase ${binding.phase}: ${binding.boundary}`;
}

function strategyLabel(registry, id) {
  return registry.strategies.find((strategy) => strategy.id === id)?.label || id;
}

function intersect(left, right) {
  const rightIds = new Set(right);
  return left.filter((id) => rightIds.has(id));
}

function formatOwners(owners) {
  return owners.map((owner) => `\`${escapeCell(owner)}\``).join('<br>');
}

function metricLabel(value) {
  return String(value).replace(/([a-z])([A-Z])/g, '$1 $2').toLowerCase();
}

function formatNumber(value) {
  return Number(value).toFixed(4).replace(/0+$/, '').replace(/\.$/, '');
}

function formatBytes(value) {
  if (!Number.isFinite(value)) return '';
  if (value === 0) return '0 download bytes';
  return `${(value / (1024 * 1024)).toFixed(1)} MiB download`;
}

function formatLatency(value, label) {
  if (!value || !Number.isFinite(value.selectionValue)) return '';
  return `${label} ${value.selectionStatistic || 'selected'} ${formatNumber(value.selectionValue)} ms`;
}

function escapeCell(value) {
  return String(value ?? '').replaceAll('|', '\\|').replaceAll('\n', ' ');
}

function main() {
  const registry = JSON.parse(fs.readFileSync(REGISTRY_PATH, 'utf8'));
  const output = renderPipelineJobMatrix(registry);
  if (process.argv.includes('--check')) {
    const current = fs.existsSync(OUTPUT_PATH) ? fs.readFileSync(OUTPUT_PATH, 'utf8') : '';
    if (current !== output) throw new Error('docs/pipeline-job-matrix.md is stale; run npm run pipeline:matrix:sync');
    process.stdout.write('Pipeline job registry and generated matrix are synchronized.\n');
    return;
  }
  fs.writeFileSync(OUTPUT_PATH, output);
  process.stdout.write('Wrote docs/pipeline-job-matrix.md.\n');
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  try {
    main();
  } catch (error) {
    console.error(error && error.stack ? error.stack : error);
    process.exitCode = 1;
  }
}
